// Порядок длин — сначала самые длинные последовательности (ZWJ, с FE0F)
const ZWJ_SEQUENCES = Object.keys(EMOJI_MAP)
  .filter(k => k.length > 2)
  .sort((a, b) => b.length - a.length);

// Одиночные эмодзи символы
const SINGLE_EMOJI = new Map();
for (const [ch, id] of Object.entries(EMOJI_MAP)) {
  if (ch.length <= 2) SINGLE_EMOJI.set(ch, id);
}

// Unicode ranges для поиска одиночных эмодзи (без ZWJ-секвенций)
const SINGLE_EMOJI_RE = /[\u{1F600}-\u{1F64F}\u{1F300}-\u{1F5FF}\u{1F680}-\u{1F6FF}\u{1F900}-\u{1F9FF}\u{1F180}-\u{1F1FF}\u{2600}-\u{27BF}\u{2300}-\u{23FF}\u{2B05}\u{2B50}\u{2B55}\u{1F000}-\u{1F02F}\u{1F0A0}-\u{1F0FF}]/gu;

/**
 * Оборачивает один эмодзи в <tg-emoji> тег для HTML.
 * @param {string} emoji - символ или последовательность эмодзи
 * @returns {string} HTML-строка с <tg-emoji> или оригинальный символ
 */
function pe(emoji) {
  if (!emoji) return '';
  const id = EMOJI_MAP[emoji];
  if (!id) return emoji;
  return `<tg-emoji emoji_id="${id}">${emoji}</tg-emoji>`;
}

/**
 * Обрабатывает текст, заменяя все эмодзи на премиум через <tg-emoji> теги.
 * Сначала ищет ZWJ-последовательности (🏃‍♂️), потом одиночные символы.
 * @param {string} text - исходный текст
 * @returns {string} текст с <tg-emoji> тегами
 */
function premiumText(text) {
  if (!text) return text;

  let result = text;

  // Заменяем ZWJ-последовательности (самые длинные первыми)
  for (const seq of ZWJ_SEQUENCES) {
    if (result.includes(seq)) {
      result = result.split(seq).join(pe(seq));
    }
  }

  // Заменяем одиночные эмодзи
  result = result.replace(SINGLE_EMOJI_RE, (match) => {
    return pe(match);
  });

  return result;
}

module.exports = { pe, premiumText, EMOJI_MAP };
